
public class MenghitungUmur {
    public static void main(String[] args) {
        
        int usiaLahir = 2004;
        
        // Menghitung umur berdasarkan tahun saat ini
        int tahunSekarang = 2024;
        int umur = tahunSekarang - usiaLahir;
        
        
        System.out.println("Hallo saya bernama eliadi zalukhu, lahir 16 Januari 2004. sekarang saya berumur : " + umur + " tahun");
    }
}